/**
 * Get an icon SVG string
 * @param {string} name - Icon name
 * @param {number} size - Icon size in pixels (default: 16)
 * @returns {string} SVG string with size applied
 */
export function getIcon(name, size = 16) {
  const icon = icons[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }

  // Add width and height attributes to the SVG
  return icon.replace('<svg', `<svg width="${size}" height="${size}"`);
}

/**
 * Get all available icon names
 * @returns {string[]} Array of icon names
 */
export function getIconNames() {
  return Object.keys(icons);
}
